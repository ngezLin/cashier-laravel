    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'product_name' => 'required',
            'sell_price' => 'required',
            'buy_price' => 'required',
            'stock' => 'required',
        ]);

        Product::create($validated);
        return redirect()->route('admin.products.index');
    }

